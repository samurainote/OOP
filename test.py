
# クラスを定義
class Human:
    age = 0
    last_name = ""
    first_name = ""
    blood_type = ""

# インスタンス化
office_lady = Human()
office_lady.age = 25

salaryman = Human()
salaryman.age = 35

senior = Human()
senior.age = 55

child = Human()
child.age = 10

print(office_lady.age)
print(salaryman.age)
print(senior.age)
print(child.age)

